// Switch Case Statement

let electronics = "Laptop";

switch (electronics) {
    case "TV":
        console.log("You selected TV");
        break;
    case "Mobile":
        console.log("You selected Mobile");
        break;
    case "Laptop":
        console.log("You selected Laptop");
        break;
    default:
        console.log(`We don't have ${electronics} in our store.`);
        break;    
}